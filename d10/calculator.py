def subtract(n1, n2):
    return n1 - n2


def multiply(n1, n2):
    return n1 * n2


def divide(n1, n2):
    return n1 / n2


app_runing = True
s = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide
}

n1 = int(input("Enter number 1"))

while app_runing:

    for item in s:
        print(item)

    op = input("Pick an oparation:")
    n2 = int(input("Enter the next number: "))

    if op == "/" and n2 <= 0:
        print("ERROR DIVIDE BY ZERO")
        continue
    else:
        tmp = s[op](n1, n2)
        print(f"{n1} {op} {n2} = {tmp}")
        n1 = tmp

    con = input("Continue ? Yes or No").lower()

    if con != "yes":
        app_runing = False
        print("Bye")
